-- Haskell has operators as follows:

a = True && True  -- a is True
b = True && False -- b is False
c = True || False -- c is True
d = not (True || False) -- d is False
e = (a == b) -- e is False
f = (a /= b) -- f is True ("/=" in Haskell is like "!=" in C)

-- Haskell also has a function called "succ", which makes it even more awesome
g = succ 68 -- nice
